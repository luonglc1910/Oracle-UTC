-- BƯỚC 1: Tạo user/schema TRA_OLONG
CREATE USER TRA_OLONG IDENTIFIED BY "TraOlong@123"
  DEFAULT TABLESPACE USERS
  TEMPORARY TABLESPACE TEMP;

-- BƯỚC 2: Cấp quyền
GRANT CONNECT, RESOURCE, DBA TO TRA_OLONG;
GRANT UNLIMITED TABLESPACE TO TRA_OLONG;

-- ============================================================
-- BƯỚC 3: Tạo các bảng (kết nối bằng user TRA_OLONG hoặc dùng prefix TRA_OLONG.)
-- ============================================================

-- 1. DANH_MUC
CREATE TABLE TRA_OLONG.DANH_MUC (
  MA_DANH_MUC  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TEN_DANH_MUC VARCHAR2(200) NOT NULL,
  MO_TA        VARCHAR2(500),
  HINH_ANH     VARCHAR2(500),
  TRANG_THAI   NUMBER(1) DEFAULT 1,
  NGAY_TAO     DATE DEFAULT SYSDATE
);

-- 2. NHA_CUNG_CAP
CREATE TABLE TRA_OLONG.NHA_CUNG_CAP (
  MA_NCC       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TEN_NCC      VARCHAR2(200) NOT NULL,
  DIA_CHI      VARCHAR2(500),
  DIEN_THOAI   VARCHAR2(20),
  EMAIL        VARCHAR2(200),
  QUOC_GIA     VARCHAR2(100),
  NGAY_HOP_TAC DATE
);

-- 3. SAN_PHAM
CREATE TABLE TRA_OLONG.SAN_PHAM (
  MA_SP        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TEN_SP       VARCHAR2(200) NOT NULL,
  MA_DANH_MUC  NUMBER,
  MA_NCC       NUMBER,
  GIA_NHAP          NUMBER(15,2),
    GIA_BAN         NUMBER(15,2),

  SO_LUONG     NUMBER DEFAULT 0,
  TRONG_LUONG NUMBER DEFAULT 0,
  MO_TA        CLOB,
  HINH_ANH     VARCHAR2(500),
  TRANG_THAI   NUMBER(1) DEFAULT 1,
  NGAY_TAO     DATE DEFAULT SYSDATE,
  CONSTRAINT FK_SP_DM  FOREIGN KEY (MA_DANH_MUC) REFERENCES TRA_OLONG.DANH_MUC(MA_DANH_MUC),
  CONSTRAINT FK_SP_NCC FOREIGN KEY (MA_NCC)      REFERENCES TRA_OLONG.NHA_CUNG_CAP(MA_NCC)
);

-- 4. KHACH_HANG
CREATE TABLE TRA_OLONG.KHACH_HANG (
  MA_KH       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  HO_TEN      VARCHAR2(200) NOT NULL,
  EMAIL       VARCHAR2(200) UNIQUE,
  DIEN_THOAI  VARCHAR2(20),
  DIA_CHI     VARCHAR2(500),
  PHUONG_XA   VARCHAR2(200),
  QUAN_HUYEN  VARCHAR2(200),
  TINH_TP     VARCHAR2(200),
  QUOC_KHAU   VARCHAR2(100),
  NGAY_SINH   DATE,
  GIOI_TINH   VARCHAR2(10),
  MAT_KHAU    VARCHAR2(500),
  TRANG_THAI  NUMBER(1) DEFAULT 1,
  NGAY_TAO    DATE DEFAULT SYSDATE
);

-- 5. NHAN_VIEN
CREATE TABLE TRA_OLONG.NHAN_VIEN (
  MA_NV        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  HO_TEN       VARCHAR2(200) NOT NULL,
  CHUC_VU      VARCHAR2(100),
  EMAIL        VARCHAR2(200) UNIQUE,
  DIEN_THOAI   VARCHAR2(20),
  TRANG_THAI   NUMBER(1) DEFAULT 1,
  LUONG        NUMBER(15,2),
  NGAY_VAO_LAM DATE
);

-- 6. KHUYEN_MAI
CREATE TABLE TRA_OLONG.KHUYEN_MAI (
  MA_KM         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TEN_KM        VARCHAR2(200) NOT NULL,
  MA_CODE       VARCHAR2(50) UNIQUE,
  PHAN_TRAM_GIAM NUMBER(5,2),
  SO_TIEN_GIAM  NUMBER(15,2),
  NGAY_BAT_DAU  DATE,
  NGAY_KET_THUC DATE,
  SO_LUONG_MA   NUMBER DEFAULT 0,
  DA_DUNG       NUMBER DEFAULT 0,
  TRANG_THAI    NUMBER(1) DEFAULT 1
);

-- 7. DON_HANG
CREATE TABLE TRA_OLONG.DON_HANG (
  MA_DH          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MA_KH          NUMBER,
  MA_NV          NUMBER,
  NGAY_DAT       DATE DEFAULT SYSDATE,
  TONG_TIEN      NUMBER(15,2),
  PHI_VAN_CHUYEN NUMBER(15,2) DEFAULT 0,
  GIAM_GIA       NUMBER(15,2) DEFAULT 0,
  TRANG_THAI     VARCHAR2(50) DEFAULT 'cho_xac_nhan',
  GHI_CHU        VARCHAR2(1000),
  DIA_CHI_GIAO   VARCHAR2(500),
  NGAY_GIAO_DK   DATE,
  CONSTRAINT FK_DH_KH FOREIGN KEY (MA_KH) REFERENCES TRA_OLONG.KHACH_HANG(MA_KH),
  CONSTRAINT FK_DH_NV FOREIGN KEY (MA_NV) REFERENCES TRA_OLONG.NHAN_VIEN(MA_NV)
);

-- 8. CHI_TIET_DH
CREATE TABLE TRA_OLONG.CHI_TIET_DH (
  MA_CTDH  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MA_DH    NUMBER NOT NULL,
  MA_SP    NUMBER NOT NULL,
  SO_LUONG NUMBER NOT NULL,
  DON_GIA  NUMBER(15,2) NOT NULL,
  CONSTRAINT FK_CTDH_DH FOREIGN KEY (MA_DH) REFERENCES TRA_OLONG.DON_HANG(MA_DH),
  CONSTRAINT FK_CTDH_SP FOREIGN KEY (MA_SP) REFERENCES TRA_OLONG.SAN_PHAM(MA_SP)
);

-- 9. THANH_TOAN
CREATE TABLE TRA_OLONG.THANH_TOAN (
  MA_TT          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MA_DH          NUMBER NOT NULL,
  PHUONG_THUC    VARCHAR2(100),
  SO_TIEN        NUMBER(15,2),
  NGAY_THANH_TOAN DATE DEFAULT SYSDATE,
  TRANG_THAI     VARCHAR2(50) DEFAULT 'cho_xu_ly',
  MA_GIAO_DICH   VARCHAR2(200),
  CONSTRAINT FK_TT_DH FOREIGN KEY (MA_DH) REFERENCES TRA_OLONG.DON_HANG(MA_DH)
);

-- 10. DANH_GIA
CREATE TABLE TRA_OLONG.DANH_GIA (
  MA_DG         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MA_KH         NUMBER,
  MA_SP         NUMBER,
  DIEM_DANH_GIA NUMBER(2,1),
  NOI_DUNG      VARCHAR2(1000),
  NGAY_DANH_GIA DATE DEFAULT SYSDATE,
  CONSTRAINT FK_DG_KH FOREIGN KEY (MA_KH) REFERENCES TRA_OLONG.KHACH_HANG(MA_KH),
  CONSTRAINT FK_DG_SP FOREIGN KEY (MA_SP) REFERENCES TRA_OLONG.SAN_PHAM(MA_SP)
);

-- 11. USER_SEQ (dùng bởi UserModel)
CREATE TABLE TRA_OLONG.USER_SEQ (
  ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  USER_NAME VARCHAR2(200) UNIQUE,
  PASS_WORD VARCHAR2(500)
);

